                    <h3 class="main-training__course-title">
                        Описание курса:
                    </h3>
                    <ul class="main-training__course-list">
                        <li>Проживание.</li>
                        <li>Теория: принцип работы паралёта, особенности эксплуатации двигателя, устранение
                            неисправностей, техника безопастности.</li>
                        <li>Теория: законодательная база, структура воздушного пространства, аэрология, выбор поля,
                            устройство параплана.</li>
                        <li>Практика: полёты с инструктором, ученик на переднем кресле; передача управления парапланом,
                            передача управления газом (специальный аппарат с ножным газом), посадки с заглушенным
                            мотором.</li>
                        <li>Практика: наземная подготовка, вывод крыла и держание с пробежкой, груз на переднем кресле,
                            первый самостоятельный вылет.</li>
                        <li>Пратика: 5-7 самостоятельных вылетов, установка параплана, сборка параплана, разборка
                            аппарата для транспортировки.</li>
                        <li>Упаковка и отправка аппарата.</li>
